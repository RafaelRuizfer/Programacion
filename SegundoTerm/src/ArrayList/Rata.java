package ArrayList;

public class Rata {
	
	private double peso;
	private int unidad;
	
	public Rata(double peso, int unidad) {
		this.peso = peso;
		this.unidad = unidad;
	}

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public int getUnidad() {
		return unidad;
	}

	public void setUnidad(int unidad) {
		this.unidad = unidad;
	}
	
	
}
